public class WordPair
{
    private String first;
    private String second;
    public WordPair(String firstWord, String secondWord){ //Constructor
        first = firstWord;
        second = secondWord;
    }
    public String getFirst(){
        return first;
    }
    
    public String getSecond(){
        return second;
    }
}
